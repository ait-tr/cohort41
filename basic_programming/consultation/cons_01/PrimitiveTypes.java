public class PrimitiveTypes {
    public static void main(String[] args) {
        // ПРИМИТИВНЫЕ ТИПЫ ДАННЫХ

        // целые числа

        byte var1;  // -128 : 127 - 1 byte
        short var2; // 2 byte
        int var3;   // 4 byte
        long var4;  // 8 byte


        // плавающая точна

        float var5;   // 4 byte
        double var6;  // 8 byte

        // символьные

        char var7;    // 2 byte

        // логические

        boolean var8;  // 1 byte

    }
}
