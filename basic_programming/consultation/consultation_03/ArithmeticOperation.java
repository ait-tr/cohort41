public class ArithmeticOperation {
    public static void main(String[] args) {

       int x = 10;
       int y = 3;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        double xDouble = 15.0;
        double yDouble = 4.0;

        System.out.println(xDouble / yDouble);
        System.out.println(xDouble % yDouble);
        // остаток от деления 15 / 4 =>
        // целочисленный остаток от деления = 3
        // остаток = 15 - 4 * 3 = 3


    }
}
